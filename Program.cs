using Microsoft.EntityFrameworkCore;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

builder.Services.AddDbContext<ASPWebAPIFirstCode.Models.EmpContext>(
    option =>
    {
        option.UseSqlServer(builder.Configuration.GetConnectionString("ConnectDB"));
    });

builder.Services.AddScoped<ASPWebAPIFirstCode.Services.IEmployeeService, ASPWebAPIFirstCode.Services.EmployeeService>();

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
